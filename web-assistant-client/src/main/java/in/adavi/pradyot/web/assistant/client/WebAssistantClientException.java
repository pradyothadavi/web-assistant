public class WebAssistantClientException extends Exception {
  
  public WebAssistantClientException() {
    super();
  }
  
  public WebAssistantClientException(String message) {
    super(message);
  }
  
  public WebAssistantClientException(String message, Throwable cause) {
    super(message, cause);
  }
  
  public WebAssistantClientException(Throwable cause) {
    super(cause);
  }
}
